//FIND AREA AND CIRCUMFERENCE OF A CIRCLE
#include<iostream>
using namespace std;

int main() {
    float r;
    cout<<"Enter radius of a circle : ";
    cin>>r;
    cout<<"Circumference of the circle is : "<<2*3.14*r<<" units"<<endl;
    cout<<"Area of the circle is : "<<3.14*r*r<<" sq. units"<<endl;
    return 0;
}
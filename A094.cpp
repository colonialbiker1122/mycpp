//Explicit type casting
#include<iostream>
using namespace std;

int main() {
    int a=34;
    float b = 25.235;
    cout<<"Value of int a :  "<<a<<endl;
    cout<<"Value of float b : "<<b<<endl;
    cout<<"Value of float(a) : "<<float(a)<<endl;
    cout<<"Value of int(b) : "<<int(b)<<endl;
    return 0;
}
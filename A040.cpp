//FIND AREA OF A TRIANGLE USING BASE AND HEIGHT
#include<iostream>
using namespace std;

int main() {
    int b,h;
    cout<<"Enter the length of base of triangle : ";
    cin>>b;
    cout<<"Enter the length of height of triangle : ";
    cin>>h;
    cout<<"Area of the triangle is : "<<0.5*b*h<<" sq. units"<<endl;
    return 0;
}